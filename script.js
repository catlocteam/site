function setLanguage(lang) {
  localStorage.setItem("lang", lang);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang][key]) {
      el.innerHTML = translations[lang][key]; // Замість textContent
    }
    const text = translations[lang][key] || key;
    el.innerHTML = text.replace(/\n/g, "<br>");
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    el.placeholder = translations[lang][key] || key;
  });

  const supportBtn = document.querySelector(".support-btn");
  if (supportBtn) {
    const newImgSrc = translations[lang].supportBtn;
    supportBtn.setAttribute("src", newImgSrc);
  }

  const imgNewLife = document.getElementById("newLife");
  if (imgNewLife) {
    imgNewLife.setAttribute("src", translations[lang].imgNewLife);
  }
  const franBowImg = document.getElementById("franBowImg");
  if (franBowImg) {
    franBowImg.setAttribute("src", translations[lang].franBowImg);
  }
  const wolfAmongUsImg = document.getElementById("wolfAmongUsImg");
  if (wolfAmongUsImg) {
    wolfAmongUsImg.setAttribute("src", translations[lang].wolfAmongUsImg);
  }
  const emmelineImg = document.getElementById("emmelineImg");
  if (emmelineImg) {
    emmelineImg.setAttribute("src", translations[lang].emmelineImg);
  }
}

document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("lang") || "ua";

  setLanguage(savedLang);

  document
    .getElementById("lang-ua")
    .addEventListener("click", () => setLanguage("ua"));
  document
    .getElementById("lang-en")
    .addEventListener("click", () => setLanguage("en"));
});

let burgerBtn = document.getElementById("burger-btn");
let headerMobile = document.getElementById("header-mobile-menu");

burgerBtn?.addEventListener("click", () => {
  if (headerMobile.style.display === "block") {
    headerMobile.style.display = "none";
  } else {
    headerMobile.style.display = "block";
  }
});

document.getElementById("toggleDonate").addEventListener("click", function () {
  document.querySelector(".donate-container").classList.toggle("show");
});
